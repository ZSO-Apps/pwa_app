export function assetUrl(urlPath) {
  try {
    const rel = urlPath.replace(/^\//, '');
    const stat = fs.statSync(path.resolve(rel));
    return `${urlPath}?v=${Math.round(stat.mtimeMs)}-${stat.size}`;
  } catch {
    return urlPath;
  }
}

export function logoAssetUrl(kind) {
  const logo = resolveLogo(kind);
  return logo ? assetUrl(logo.url) : '';
}

export function renderPrintLogo() {
  const logo = logoAssetUrl('print');
  if (!logo) return '';
  return `<div class="print-logo-wrap"><img class="print-logo" src="${esc(logo)}" alt="ZSO App"></div>`;
}

export function formatDateTime(value) {
  if (!value) return '';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return String(value);
  return new Intl.DateTimeFormat('de-CH', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
    timeZone: 'Europe/Zurich',
  }).format(date) + ' Uhr';
}

